    public void readFile(String url) {
        BufferedReader r;
        try {
            File txt = new File(url);
            r = new BufferedReader(new FileReader(txt));
            String line = null;
            while ((line = r.readLine()) != null) {
                String[] obj = line.split(",");
                for (int i = 0; i < obj.length; i ++) {
                    System.out.print("" + obj[i] + "\n");
                    String palabra[] = obj[i].split(" ");
                    for (int x = 0; x < palabra.length; x ++) {
                        System.out.print("" + palabra[x] + "\n");
                        Palabra p = new Palabra(
                                palabra[0],
                                palabra[1],
                                palabra[2]
                        );
                        palabras.add(p);
                    }
                }
            }
            r.close();
        } catch (Exception ex) {
            Logger.getLogger(UserView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
    private void buscarPalabra(String palabra, String idioma) {
        for (int i = 0; i < palabras.size(); i++) {
            switch (idioma) {
                case "EspaÃ±ol":
                    if (palabras.get(i).getEspanol().equals(palabra.trim().toLowerCase())) {
                        System.out.print(i);
                    }
                    break;
                case "Ingles":
                    if (palabras.get(i).getEspanol().equals(palabra.trim().toLowerCase())) {
                        System.out.print("ingles " + palabras.get(i).getIngles());
                        jtfResultado.setText(palabras.get(i).getIngles());
                        return;
                    }
                    break;
                case "Frances":
                    if (palabras.get(i).getEspanol().equals(palabra.trim().toLowerCase())) {
                        System.out.print(palabras.get(i).getFrances());
                        jtfResultado.setText(palabras.get(i).getFrances());
                        return;
                    }
                    break;
            }
        }
    }
    
    
